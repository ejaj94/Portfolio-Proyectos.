// Helper: render text with italic brand name AND line breaks
function formatItalicText(text) {
    // Convert \n to <br> then wrap brand name in italic
    return text
        .replace(/\n/g, "<br><br>")
        .replace(/Com Cheiro de Amor/g, "<i>Com Cheiro de Amor</i>");
}

// Render materials comma-separated string into HTML list items
function renderMaterialsList(rawString) {
    const listElement = document.getElementById("text-materials-list");
    listElement.innerHTML = "";
    
    const materials = rawString.split(/[,\n]/).map(item => item.trim()).filter(item => item.length > 0);
    
    materials.forEach(material => {
        const li = document.createElement("li");
        // Capitalize first letter of each item
        let formatted = material.charAt(0).toUpperCase() + material.slice(1);
        // Ensure it ends with a dot
        if (!formatted.endsWith('.')) {
            formatted += '.';
        }
        li.innerText = formatted;
        listElement.appendChild(li);
    });
}

// Handle changes in input fields in real-time
function updateText(elementId, value) {
    // Update local dictionary so it persists when switching languages back and forth
    contentData[currentLanguage][elementId] = value;
    
    // Map text element
    const previewMap = {
        'cover-title': 'text-cover-title',
        'cover-subtitle': 'text-cover-subtitle',
        'cover-slogan': 'text-cover-slogan',
        'story-title': 'text-story-title',
        'story-body': 'text-story-body',
        'story-quote': 'text-story-quote',
        'workshop-title': 'text-workshop-title',
        'workshop-body': 'text-workshop-body',
        'materials-title': 'text-materials-title',
        'notes-title': 'text-notes-title',
        'notes-subtitle': 'text-notes-subtitle',
        'promo-title': 'text-promo-title',
        'promo-body': 'text-promo-body',
        'contact-text': 'text-contact-text'
    };
    
    const targetElementId = previewMap[elementId];
    if (targetElementId) {
        const el = document.getElementById(targetElementId);
        if (elementId === 'story-body' || elementId === 'workshop-body') {
            el.innerHTML = formatItalicText(value);
        } else {
            el.innerText = value;
        }
    }
}

// Special handler for materials text area
function updateMaterialsList(value) {
    contentData[currentLanguage]["materials-body"] = value;
    renderMaterialsList(value);
}

// Switch between languages (Portuguese and English)
function setLanguage(lang) {
    currentLanguage = lang;
    
    // Toggle active state on buttons
    document.getElementById("btn-lang-pt").classList.toggle("active", lang === 'pt');
    document.getElementById("btn-lang-en").classList.toggle("active", lang === 'en');
    
    // Reset fields in the editor
    resetEditorFields();
    
    // Rerender all preview values
    renderAllPreviewTexts();
    renderMaterialsList(contentData[lang]["materials-body"]);
}

// Select a visual theme (1 of 10 examples)
function selectStyle(styleName) {
    currentStyle = styleName;
    
    // Remove active state from all style cards
    const cards = document.querySelectorAll(".style-card");
    cards.forEach(card => {
        card.classList.toggle("active", card.getAttribute("data-style") === styleName);
    });
    
    // Change style classes on the preview sheet
    const sheet = document.getElementById("pamphlet-sheet");
    sheet.className = `brochure-sheet style-${styleName}`;
}

// Switch between Inside Content and Outside Covers
function toggleFace(face) {
    currentFace = face;
    
    // Toggle active class on tab buttons
    document.getElementById("btn-face-inside").classList.toggle("active", face === 'inside');
    document.getElementById("btn-face-outside").classList.toggle("active", face === 'outside');
    
    // Toggle hidden classes on the preview faces
    const faceInside = document.getElementById("face-inside");
    const faceOutside = document.getElementById("face-outside");
    
    if (face === 'inside') {
        faceInside.classList.remove("hidden");
        faceOutside.classList.add("hidden");
    } else {
        faceInside.classList.add("hidden");
        faceOutside.classList.remove("hidden");
    }
}

